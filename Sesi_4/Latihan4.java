        int b = 8;
        int c = 12;
        int d = 5;

        System.out.println("Tes ke - 1 = " + (a > b));
        System.out.println("Tes ke - 2 = " + (a < c));
        System.out.println("Tes ke - 3 = " + (c >= d));
        System.out.println("Tes ke - 4 = " + (d <= a));
